using System;              // 命名空间，方便调用系统功能
#nullable disable          // 关闭空值警告，让代码更简单

class Dool                 // 创建一个叫 Dool 的类
{
    public event Action Kin;   // 创建一个叫 Kin 的事件（盒子）

    public string Kai { get; set; }   // 创建一个叫 Kai 的属性，用来存卡号

    public void Swipe()      // 创建一个叫 Swipe 的方法（刷卡动作）
    {
        Console.WriteLine("刷卡中" + "卡号" + Kai);   // 方法1：输出刷卡信息

        Console.WriteLine("刷卡成功！");   // 方法2：输出成功信息

        Kin?.Invoke();       // 触发事件盒子，执行盒子里所有方法
    }
}


class Kiu                  // 创建一个叫 Kiu 的类（用来关联和运行）
{
    public static void Sun()   // 创建一个叫 Sun 的静态方法（程序启动后运行这里）
    {
        Dool myDool = new Dool();   // 创建 Dool 类的实例（让代码真正动起来）

        myDool.Kai = "A8888";       // 给 Kai 属性赋值，卡号是 A8888

        myDool.Kin += UN;           // 把 UN 方法放进 Kin 事件盒子
        myDool.Kin += Nu;           // 把 Nu 方法放进 Kin 事件盒子

        myDool.Swipe();             // 调用 Swipe 方法（开始刷卡）
    }

    public static void UN()    // 创建一个叫 UN 的静态方法（放进盒子里用）
    {
        Console.WriteLine("识别到最高级别权限！！");   // 方法1

        Console.WriteLine("门已开，请确保周围环境正常情况下进入基地。");   // 方法2
    }

    public static void Nu()    // 创建一个叫 Nu 的静态方法（放进盒子里用）
    {
        Console.WriteLine("欢迎您再次回到这里！");   // 方法
    }

    class Program       // 创建一个叫 Program 的类（程序入口）
    {
        static void Main()   // 创建一个叫 Main 的静态方法（程序从这里开始运行）
        {
            Kiu.Sun();   // 调用 Kiu 类里的 Sun 方法，启动整个程序
        }
    }
}

